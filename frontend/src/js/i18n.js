  const SUPPORTED = ["en", "es", "pt"];
  const DEFAULT_LANG = "es";
  const STORAGE_KEY = "smokeping_easy_lang";

  function detectLang() {
    try {
      const stored = localStorage.getItem(STORAGE_KEY);
      if (stored && SUPPORTED.includes(stored)) return stored;
    } catch (e) { /* ignore */ }
    const nav = (navigator.language || navigator.userLanguage || "").toLowerCase();
    const short = nav.split("-")[0];
    if (SUPPORTED.includes(short)) return short;
    return DEFAULT_LANG;
  }

  function translate(lang, key, params) {
    const table = TRANSLATIONS[lang] || TRANSLATIONS[DEFAULT_LANG];
    let str = table[key];
    if (str === undefined) {
      // Fall back to English, then to the key itself (visible = missing key).
      str = (TRANSLATIONS.en[key] !== undefined) ? TRANSLATIONS.en[key] : key;
    }
    if (params) {
      str = str.replace(/\{(\w+)\}/g, (m, k) =>
        (params[k] !== undefined ? String(params[k]) : m)
      );
    }
    return str;
  }

  // Global helper usable outside Alpine (e.g. api.js error mapping).
  const I18n = {
    supported: SUPPORTED,
    lang: detectLang(),
    t(key, params) { return translate(this.lang, key, params); },
    setLang(lang) {
      if (!SUPPORTED.includes(lang)) return;
      this.lang = lang;
      try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) { /* ignore */ }
      document.documentElement.setAttribute("lang", lang);
      if (window.Alpine && Alpine.store("i18n")) Alpine.store("i18n").lang = lang;
    }
  };
  window.I18n = I18n;

  document.documentElement.setAttribute("lang", I18n.lang);

  // Register the reactive Alpine store when Alpine initialises.
  document.addEventListener("alpine:init", () => {
    Alpine.store("i18n", {
      lang: I18n.lang,
      supported: SUPPORTED,
      t(key, params) { return translate(this.lang, key, params); },
      setLang(lang) {
        if (!SUPPORTED.includes(lang)) return;
        this.lang = lang;
        I18n.lang = lang;
        try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) { /* ignore */ }
        document.documentElement.setAttribute("lang", lang);
      }
    });
    // Convenience magic: $t('key', {params})
    Alpine.magic("t", () => (key, params) => Alpine.store("i18n").t(key, params));
  });
